public class CharacterLiteralEx {

	public static void main(String[] args) {
		char c='h'; //Single quote and single digit
//		char C="s";//Error
//		char c='sdaasd';//Error
		char c1=104;		//ASCII code 0-255
		char c2='\u0068';	//Uni code 	0-65535
		System.out.println(c);
		System.out.println(c1);
		System.out.println(c2);
		
//		ASCII CODES(0-255)   -UNI CODE(0-65535)
		//A-Z= 65-90
		//a-z= 97-122
		//0-9= 48-57
		//space-32
		//Enter-10
	}

}