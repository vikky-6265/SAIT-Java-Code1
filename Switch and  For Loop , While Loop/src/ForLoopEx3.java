public class ForLoopEx3 {
	public static void main(String[] args) {
		int a=1;
		for(;++a<=5;) {
			System.out.println("Sukesh Pandey");
		}
		System.out.println(a);
		System.out.println("---------");
		int x=1;
		for(;x++<=5;) {
			System.out.println("Sukesh Pandey");
		}
		System.out.println(x);
	}
}