public class SwitchEx5 {

	public static void main(String[] args) {
		char x='a';
		switch(x) {
			case 97:
				System.out.println("Hello");
				break;
			case 'a':
				System.out.println("Hi");
				break;
			default:
				System.out.println("Invalid Number");
		}
		System.out.println("Thanks for using APP");
	}

}